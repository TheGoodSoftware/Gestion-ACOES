	public int getProyecto() {
		BD bd = new BD();
		Map<Integer, String> proyectos = bd.getAllProyectos();
		for(Integer key : proyectos.keySet()) {
			if(proyectos.get(key).equals(this.proyectoCampo.getSelectedItem().toString())) {
				return key;
			}
		}
		return -1;
	}
